function changeLanguage(lang) {
  localStorage.setItem('selectedLanguage', lang)

  document.getElementById('todos').innerHTML = homepagecontent[lang].todos;
  document.getElementById('text_atlas').innerHTML = homepagecontent[lang].text_atlas;
  document.getElementById('title_atlas_tecnologia').innerHTML = homepagecontent[lang].title_atlas_tecnologia;
  document.getElementById('subtitle_atlas_tecnologia').innerHTML = homepagecontent[lang].subtitle_atlas_tecnologia;
  document.getElementById('text_atlas_tecnologia').innerHTML = homepagecontent[lang].text_atlas_tecnologia;
  document.getElementById('title_atlas_tecnologia2').innerHTML = homepagecontent[lang].title_atlas_tecnologia2;
  document.getElementById('subtitle_atlas_tecnologia2').innerHTML = homepagecontent[lang].subtitle_atlas_tecnologia2;
  document.getElementById('text_atlas_tecnologia2').innerHTML = homepagecontent[lang].text_atlas_tecnologia2;
  document.getElementById('mina').innerHTML = homepagecontent[lang].mina;
  document.getElementById('text_mina').innerHTML = homepagecontent[lang].text_mina;
  document.getElementById('entregaveis').innerHTML = homepagecontent[lang].entregaveis;
  document.getElementById('text_entregaveis').innerHTML = homepagecontent[lang].text_entregaveis;
  document.getElementById('beyond').innerHTML = homepagecontent[lang].beyond;
  document.getElementById('acesso_online').innerHTML = homepagecontent[lang].acesso_online;
  document.getElementById('text_acesso_online').innerHTML = homepagecontent[lang].text_acesso_online;
  document.getElementById('analise_dados').innerHTML = homepagecontent[lang].analise_dados;
  document.getElementById('text_analise_dados').innerHTML = homepagecontent[lang].text_analise_dados;
  document.getElementById('download_dados').innerHTML = homepagecontent[lang].download_dados;
  document.getElementById('text_download_dados').innerHTML = homepagecontent[lang].text_download_dados;
  document.getElementById('sistema_consulta').innerHTML = homepagecontent[lang].sistema_consulta;
  document.getElementById('text_sistema_consulta').innerHTML = homepagecontent[lang].text_sistema_consulta;
  document.getElementById('camadas_extras').innerHTML = homepagecontent[lang].camadas_extras;
  document.getElementById('text_camadas_extras').innerHTML = homepagecontent[lang].text_camadas_extras;
  document.getElementById('ressonancia_radiometria').innerHTML = homepagecontent[lang].ressonancia_radiometria;
  document.getElementById('monitoramento').innerHTML = homepagecontent[lang].monitoramento;
  document.getElementById('geofisica').innerHTML = homepagecontent[lang].geofisica

  const servicos = document.getElementsByClassName('servicos');
  for (let i = 0; i < servicos.length; i++) {
    servicos[i].innerText = homepagecontent[lang].servicos;
  }

  const galeria = document.getElementsByClassName('galeria');
  for (let i = 0; i < galeria.length; i++) {
    galeria[i].innerText = homepagecontent[lang].galeria;
  }

  const contato = document.getElementsByClassName('contato');
  for (let i = 0; i < contato.length; i++) {
    contato[i].innerText = homepagecontent[lang].contato;
  }

  const imagesinssar = document.getElementById('imagesinsar')
  if(lang === "es"){
    imagesinssar.src = "./images/insar-sixense-portugues.png"
  }else if(lang === "en"){
    imagesinssar.src = "./images/insar-sixense-ingles.png"
  }else {
    imagesinssar.src = "./images/insar-sixense-portugues.png"
  }
}

window.addEventListener('DOMContentLoaded', () => {
  const savedLanguage = localStorage.getItem('selectedLanguage') || 'pt';
  changeLanguage(savedLanguage);
});